export function getLocale(): Locale {
  if (typeof window === 'undefined') return 'fr';
  return (localStorage.getItem('immopro_locale') as Locale) || 'fr';
}

export function setLocale(locale: Locale) {
  localStorage.setItem('immopro_locale', locale);
  window.dispatchEvent(new CustomEvent('immopro:locale-changed', { detail: { locale } }));
}

export function t(key: keyof typeof translations.fr, locale?: Locale): string {
  const current = locale || getLocale();
  return (translations[current] as any)[key] || (translations.fr as any)[key] || key;
}

export type { Locale };
